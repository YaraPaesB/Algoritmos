idade = int(input("Digite a idade: "))

if idade >= 5 and idade <= 7:
    print("Esse nadador é da categoria infantil.")
elif idade >= 8 and idade <= 10:
    print("Esse nadador é da categoria juvenil.")
elif idade >= 11 and idade <= 15:
    print("Esse nadador é da categoria adolescente.")
elif idade >= 16 and idade <= 30:
    print("Esse nadador é da categoria adulto.")
elif idade > 30:
    print("Esse nadador é da categoria sênior.")




